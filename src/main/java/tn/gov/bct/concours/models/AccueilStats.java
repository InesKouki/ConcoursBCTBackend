package tn.gov.bct.concours.models;


public class AccueilStats {

	private Long concoursTotal;
	private int concoursActive;
	private Long users;
	private Long usersConfirmed;
	
	
	public 	Long getConcoursTotal() {
		return concoursTotal;
	}
	public void setConcoursTotal(Long concoursTotal) {
		this.concoursTotal = concoursTotal;
	}
	public Long getUsers() {
		return users;
	}
	public void setUsers(Long users) {
		this.users = users;
	}
	public Long getUsersConfirmed() {
		return usersConfirmed;
	}
	public void setUsersConfirmed(Long usersConfirmed) {
		this.usersConfirmed = usersConfirmed;
	}
	public int getConcoursActive() {
		return concoursActive;
	}
	public void setConcoursActive(int concoursActive) {
		this.concoursActive = concoursActive;
	}
	
	
	
	
	
}
